#include "Coffee_bean.h"


void expand_2x2(dbfl I[ROW+4][COL+4])
{

	uint16 i, j;

	for (i = 2; i <= COL + 2; i++)
	{
		I[1][i]				= I[2][i];
		I[0][i]				= I[3][i];
		I[ROW + 3][i] = I[ROW + 0][i];
		I[ROW + 2][i] = I[ROW + 1][i];
	}
	for (j = 2; j <= ROW+2; j++)
	{
		I[j][1] = I[j][2];
		I[j][0] = I[j][3];
		I[j][COL + 3] = I[j][COL + 0];
		I[j][COL + 2] = I[j][COL + 1];
	}
	//=============================================// I
	I[0][0] = I[0][3];
	I[0][1] = I[0][2];
	I[1][0] = I[1][3];
	I[1][1] = I[1][2];

	//=============================================// II
	I[0][COL + 3] = I[0][COL + 0];
	I[0][COL + 2] = I[0][COL + 1];
	I[1][COL + 3] = I[1][COL + 0];
	I[1][COL + 2] = I[1][COL + 1];

	//=============================================// III
	I[ROW + 3][0] = I[ROW + 0][0];
	I[ROW + 2][0] = I[ROW + 1][0];
	I[ROW + 3][1] = I[ROW + 0][1];
	I[ROW + 2][1] = I[ROW + 1][1];

	//=============================================// IV
	I[ROW + 3][COL + 3] = I[ROW    ][COL + 3];
	I[ROW + 2][COL + 3] = I[ROW + 1][COL + 3];
	I[ROW + 3][COL + 2] = I[ROW    ][COL + 2];
	I[ROW + 2][COL + 2] = I[ROW + 1][COL + 2];
}
